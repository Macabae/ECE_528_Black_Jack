static int template_score(const uint8_t img[DIGIT_H][DIGIT_W],
                          const uint8_t tpl[DIGIT_H][DIGIT_W])
{
    int score = 0;
    int y, x;

    for (y = 0; y < DIGIT_H; y++)
    {
        for (x = 0; x < DIGIT_W; x++)
        {
            if (img[y][x] != tpl[y][x])
            {
                score++;
            }
        }
    }
    return score;
}

/*
 * Capture a tiny grayscale-ish frame.
 * We keep only one byte per pixel for simplicity.
 * This assumes your camera is already outputting a small RGB mode
 * and your sync pins are wired as:
 *   VSYNC=P2.5 HREF=P2.6 PCLK=P2.7
 */
void capture_frame_gray(void)
{
    int row, col;

    while (P2->IN & BIT5);
    while (!(P2->IN & BIT5));
    while (P2->IN & BIT5);

    for (row = 0; row < FRAME_H; row++)
    {
        while (!(P2->IN & BIT6));

        for (col = 0; col < FRAME_W; col++)
        {
            while (!(P2->IN & BIT7));
            frame[row][col] = cam_read_byte();
            while (P2->IN & BIT7);

            while (!(P2->IN & BIT7));
            (void)cam_read_byte();   // discard second RGB565 byte
            while (P2->IN & BIT7);
        }

        while (P2->IN & BIT6);
    }
}

void threshold_roi(uint8_t threshold)
{
    int y, x;
    for (y = 0; y < ROI_H; y++)
    {
        for (x = 0; x < ROI_W; x++)
        {
            uint8_t p = frame[ROI_Y + y][ROI_X + x];
            roi_bin[y][x] = (p < threshold) ? 1 : 0; // dark ink = 1
        }
    }
}

void downsample_roi_to_digit(void)
{
    int dy, dx;
    int src_y0, src_y1, src_x0, src_x1;
    int y, x;
    int sum, count;

    for (dy = 0; dy < DIGIT_H; dy++)
    {
        for (dx = 0; dx < DIGIT_W; dx++)
        {
            src_y0 = (dy * ROI_H) / DIGIT_H;
            src_y1 = ((dy + 1) * ROI_H) / DIGIT_H;
            src_x0 = (dx * ROI_W) / DIGIT_W;
            src_x1 = ((dx + 1) * ROI_W) / DIGIT_W;

            sum = 0;
            count = 0;

            for (y = src_y0; y < src_y1; y++)
            {
                for (x = src_x0; x < src_x1; x++)
                {
                    sum += roi_bin[y][x];
                    count++;
                }
            }

            digit_img[dy][dx] = (sum * 2 >= count) ? 1 : 0;
        }
    }
}

int recognize_digit(void)
{
    int best_digit = -1;
    int best_score = 999999;
    int s;

    const uint8_t (*templates[10])[DIGIT_W] = {
        tpl_0, tpl_1, tpl_2, tpl_3, tpl_4,
        tpl_5, tpl_6, tpl_7, tpl_8, tpl_9
    };

    int d;
    for (d = 0; d < 10; d++)
    {
        s = template_score(digit_img, templates[d]);
        if (s < best_score)
        {
            best_score = s;
            best_digit = d;
        }
    }

    return best_digit;
}


